            <div class="item__name">Fitness is not about being better than someone else</div>
            <div class="item__desc">There’s nothing but truth in this quote… Progress can only be measured against oneself. The greatest athletes of our time do not use their competition to measure their success. (Even though it appears that way!) They measure their success by how they themselves have improved their skill and ability. It’s the sports viewer that places supreme importance on being better than the opponent. I can lose a race/match/game, but it will still be a success if I have achieved a new level of performance… even with the “Loss”. <br> <br>
                Fitness, like competition, is an athletic challenge. The goal in both is to become a better “You” than when “You” began. Keep your eye on the prize just like the elite athlete! The prize is not the Gold Medal, or Championship Trophy. Those are the possible byproducts of the ultimate prize: To make oneself BETTER!  <br> <br>
